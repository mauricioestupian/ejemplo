      <h2 align="center">Estudiar técnico en Programación de Software del SENA</h2>
       <P> <img src="imagen/software.jpg" width="34%" height="auto" align="left"> programa Técnico en Programación de Software se creó para brindar al<span class="pp"> sector</span> productivo nacional en general (debido a que la Industria del Software aplica para la mejora de los procesos productivos en todos los sectores ya sea industria, comercio, servicios, sector primario y extractivo, etc), la posibilidad de incorporar personal con altas calidades laborales y profesionales que contribuyan al desarrollo económico, social y tecnológico de su entorno y del país, así mismo ofrecer a los aprendices formación en las tecnologías relacionados con el ciclo de vida del Software incluyendo las fases de Análisis y Desarrollo de Software, factores muy importantes para la competitividad y el efectivo posicionamiento de esta industria en el país.</p>

