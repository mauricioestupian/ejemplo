       <p>Dentro de los requisitos para inscribirte a la carrera técnica de Progamación de Software, debes contar con un nivel educativo de básica secundaria, realizar la inscripción en las fechas estipuladas y presentar y aprobar oportunamente las pruebas de selección fase I y II y/o taller</p>

